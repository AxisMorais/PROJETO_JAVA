package LISTA_4;

//Implemente um algoritmo que imprima todos os números inteiros do intervalo fechado de 1 a 100.

public class Thiago_Morais_01 {
   // Contando de um  até 100
	 public static void main(String[] args) {
		
		for (  int i = 0   ;  i <= 100   ; i++   ) {
			
			System.out.println("Contando " + i);
			i=i++;
		}
		 
		 
	 }
}
