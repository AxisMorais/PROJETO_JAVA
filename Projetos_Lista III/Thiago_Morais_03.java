package LISTA_4;

//Escreva um algoritmo que imprima os números de 100 à 1 (ordem decrescente).

public class Thiago_Morais_03 {

	// Contando de 100 até 1 ; 
	
	 public static void main(String[] args) {
		
		 for (  int i = 100   ;  i >= 1   ; i--   ) {
				
				System.out.println("Contando " + i);
				i=i++;
			}
		 
		 
	 }
}
