package LISTA_4;

//Escreva um programa que imprima os números pares de 0 à 50. Utilize a estrutura for.

public class Thiago_Morais_02 {
	
 // Contando de um até 50
	
	 public static void main(String[] args) {
			
		 
		 for (  int i = 0   ;  i <= 50  ; i++   ) {
				
				System.out.println( i);
				i=i++;
			}
		 
	 }
}
